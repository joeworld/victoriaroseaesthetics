                    <p>The reason why EyEnvy® is best to be used in the morning is because studies have shown that when products near the eyes are applied at night, they have an increased chance of sensitivity to product due to various factors. When used in the morning the eye’s lacrimal glands are more active, sensitivity is reduced leading to a healthier experience with any product. Because we are unaware of the sensitivity levels of all clients we have created a general rule for everyone to use the product in the morning.</p>
                    <p class="product-subtitle">CAN I APPLY MAKEUP AFTER USING EYENVY®?</p>
                    <p>Yes, you can use all forms of make-up after applying EyEnvy®. We suggest that you first apply EyEnvy® to a clean lid and then let it dry for 1-2 minutes. You may then apply make-up as usual.</p>
                    <p class="product-subtitle">CAN I PUT ON MASCARA AFTER APPLYING EYENVY®?</p>
                    <p>Yes. Mascara can be used 1-2 minutes after applying the serum. It is recommended to use the EyEnvy® mascara as it will also condition the lashes.</p>
