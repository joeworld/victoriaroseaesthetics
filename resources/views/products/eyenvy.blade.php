                    <p>Yes. Because the product is applied to the skin and not the lashes it will not interfere with the bond of the glue. Make sure to wait 48 hours after your refill or full-set to allow the glue to adhere well. Many lash technicians recognize the benefits of using a lash conditioner with lash extensions for many reasons. The benefits of adding vitamins to the root of your lashes help to strengthen the bulb, disallowing the hair follicle from prematurely falling (due to the extra weight of an artificial lash). Due to the added length and volume of your lashes the technician can successfully increase more lash placement. Clients who want longer lashes but are limited due to their very short lashes can achieve longer length results because their lashes are long enough for the technician to apply even longer lashes which will adhere. Due to the increased density the technician may apply thicker lashes. We do recommend applying the product every other day while you are wearing individual lash extensions.</p>
